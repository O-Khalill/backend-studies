/*6. Use a switch statement to return the day of the week given a number (1 = Sunday ...., 7 = Saturday). (0.5 Grade)
• Input Example: 2
• Output Example: “Monday”*/
let number = 2;

switch (number) {
	case 1:
		console.log("Sunday");
		break;
	case 2:
		console.log("Monday");
		break;
	case 3:
		console.log("Tuesday");
		break;
	case 4:
		console.log("Wednesday");
		break;
	case 5:
		console.log("Thursday");
		break;
	case 6:
		console.log("Friday");
		break;
	case 7:
		console.log("Saturday");
		break;
}
